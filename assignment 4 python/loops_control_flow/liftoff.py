def main():
    # Loop from 10 down to 1
    for i in range(10, 0, -1):
        print(i, end=" ")  # Print the current countdown number

    print("Liftoff!")  # Print "Liftoff!" after the countdown

# This line is required to call the main() function when the script is executed
if __name__ == '__main__':
    main()
